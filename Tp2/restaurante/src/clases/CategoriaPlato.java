package clases;

/**
 * Enumeración que representa las categorías de un plato en un menú.
 * <ul>
 *     <li>{@link #ENTRADA} - Un plato que se sirve como entrada.</li>
 *     <li>{@link #PLATO_PRINCIPAL} - El plato principal de la comida.</li>
 *     <li>{@link #POSTRE} - Un plato dulce que se sirve como postre.</li>
 * </ul>
 */
public enum CategoriaPlato {
    
    /** Un plato que se sirve como entrada. */
    ENTRADA,

    /** El plato principal de la comida. */
    PLATO_PRINCIPAL,

    /** Un plato dulce que se sirve como postre. */
    POSTRE;
}

